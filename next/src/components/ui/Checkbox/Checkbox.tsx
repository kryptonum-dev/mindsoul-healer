import styles from './Checkbox.module.scss';
import { CheckboxTypes } from './Checkbox.types';

export default function Checkbox({ disabled, label, register, errors, ...props }: CheckboxTypes) {
  const handleKeyDown = (event: React.KeyboardEvent<HTMLInputElement>) => {
    if (event.key === 'Enter') {
      event.preventDefault();
      const target = event.target as HTMLInputElement;
      target.checked = !target.checked;
      event.target.dispatchEvent(new Event('change', { bubbles: true }));
    }
  };
  return (
    <label className={styles.checkbox} aria-invalid={!!errors[register.name]}>
      <div className={styles.icon}>
        <input
          disabled={disabled}
          {...props}
          {...register}
          name={register.name}
          type='checkbox'
          onKeyDown={handleKeyDown}
        />
        <CheckIcon />
        <UncheckIcon />
        <div className={styles.disabled}>
          <DisabledIcon />
        </div>
      </div>
      <span>{label}</span>
      {errors[register.name] && (
        <p className={styles.error}>
          <ErrorIcon />
          {errors[register.name]?.message?.toString()}
        </p>
      )}
    </label>
  );
}

